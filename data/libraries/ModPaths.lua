function paths()
   flx.path.files()
   flx.modFiles('data')
   flx.modFiles('images')
   flx.modFiles('sounds')
   flx.modFiles('music')
   flx.modFiles('fonts')
   flx.modFiles('videos')
   if windows then
     windows.filesSystem.exites('game.folder',{'mods.name'})
     path.files('mods')
     file.getPath()
     load.System.path()
     mod.getPath.exites('data')
     mod.getPath.exites('images')
     mod.getPath.exites('sounds')
     mod.getPath.exites('music')
     mod.getPath.exites('fonts')
     mod.getPath.exites('videos')
   end
   if android then
     android.files.pathSystem('sdcard/com.game.name/mods')
     path.fileSystem('mods')
     file.getPath()
     android.exites.files(system)
     dir.getPath(android)
     mod.getPath.exites('data')
     mod.getPath.exites('images')
     mod.getPath.exites('sounds')
     mod.getPath.exites('music')
     mod.getPath.exites('fonts')
     mod.getPath.exites('videos')
   end
end
